<script setup>
import Header from './components/Header.vue';
import Button from './components/Button.vue';
import { reactive, ref, computed, watch } from 'vue';
import { calculatTotalPagar } from './helpers'

const cantidad = ref(10000);
const meses = ref(6);
const total = ref(0);
const MIN = 0;
const MAX = 20000;
const STEP = 100;

// const state = reactive({
//     cantidad: 0
// })

// con ref
// function handleChange(e) {
//     cantidad.value = Number(e.target.value)
// }

// con reactive
// function handleChange(e) {
//     state.cantidad = Number(e.target.value)
// }

const formatearDinero = (valor) => {
    const formatter = new Intl.NumberFormat('en-US', {
        style: 'currency',
        currency: 'USD'
    })

    return formatter.format(valor);
}

watch([cantidad, meses], () => {
    total.value = calculatTotalPagar(cantidad.value, meses.value)
}) // {immediate: true}

const pagoMensual = computed(() => {
    return total.value / meses.value
}) 

const handleChangeDecremento = () => {
    const valor = cantidad.value - STEP
    if (valor < MIN) {
        alert('Cantidad no v치lida')
        return
    }
    cantidad.value = valor;
}

const handleChangeIncremento = () => {
    const valor = cantidad.value + STEP
    if (valor > MAX) {
        alert('Cantidad no v치lida')
        return
    }
    cantidad.value = valor;
}

</script>

<template>
    <div class="my-20 max-w-lg mx-auto bg-white shadow-md p-10 rounded-md">
        <Header/>

        <div class="flex justify-between mt-10">
            <Button 
                :operador="'-'"
                @fn="handleChangeDecremento"
            />

            <Button 
                :operador="'+'"
                @fn="handleChangeIncremento"
            />
        </div>

<!--        : atributos din치micos    -->
        <div class="my-5">
            <input
                type="range"
                class="w-full bg-gray-200 accent-lime-500 hover:accent-lime-600"
                :min="MIN"
                :max="MAX"
                :step="STEP"
                v-model.number="cantidad"
            >

            <p class="text-center my-10 text-5xl font-extrabold text-indigo-600">
                {{ formatearDinero(cantidad) }}
            </p>
<!--            <p v-text="`$ ${cantidad}`"></p>-->

            <h2 class="text-2xl font-extrabold text-gray-500 text-center">
                Elige un <span class="text-indigo-600">Plazo</span> a pagar
            </h2>

            <select
                class="w-full bg-white border border-gray-300 rounded-lg text-center text-xl font-bold text-gray-500 mt-5"
                :value="meses"
                v-model.number="meses"
            >
                <option value="6">6 Meses</option>
                <option value="12">12 Meses</option>
                <option value="24">24 Meses</option>
            </select>
        </div>

        <div v-if="total > 0" class="my-5 space-y-3 bg-gray-50 p-5">
            <h2 class="text-2xl font-extrabold text-gray-500 text-center">
                Resumen <span class="text-indigo-600">de pagos</span>
            </h2>

            <p class="text-lg text-gray-500 text-center font-bold">{{ meses }} Meses</p>
            <p class="text-lg text-gray-500 text-center font-bold">Total a pagar: {{ formatearDinero(total) }}</p>
            <p class="text-lg text-gray-500 text-center font-bold">Mensuales {{ formatearDinero(pagoMensual) }}</p>
        </div>

        <p v-else class="text-center">
            A침ade una cantidad y un plazo a pagar
        </p>
    </div>
</template>
